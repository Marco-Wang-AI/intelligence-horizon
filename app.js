function median(values) {
  const sorted = values.filter(Number.isFinite).sort((a, b) => a - b);
  if (!sorted.length) return 0;
  const mid = Math.floor(sorted.length / 2);
  return sorted.length % 2 ? sorted[mid] : (sorted[mid - 1] + sorted[mid]) / 2;
}

function numericAnswers(votes, key) {
  return votes
    .map((vote) => vote[key])
    .filter((value) => value !== null && value !== undefined && value !== "")
    .map(Number);
}

function formatYear(value) {
  return String(Math.round(Number(value)));
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function absoluteUrl(path) {
  return new URL(path, window.location.href).href;
}

function formatDelta(meters) {
  if (language === "zh") {
    const distance = `${Math.abs(meters)} 米`;
    return meters > 0 ? `远离 ${distance}` : `靠近 ${distance}`;
  }
  const distance = `${Math.abs(meters)}m`;
  return meters > 0 ? `${distance} farther` : `${distance} closer`;
}

function eventTimeValue(event) {
  if (!event?.date || event.date === "Always") return -Infinity;
  const parts = String(event.date).split("-").map(Number);
  const year = Number.isFinite(parts[0]) ? parts[0] : 0;
  const month = Number.isFinite(parts[1]) ? parts[1] : 12;
  return year * 100 + month;
}

function sortedEvents() {
  return events
    .slice()
    .sort((a, b) => eventTimeValue(b) - eventTimeValue(a) || Math.abs(b.deltaMeters) - Math.abs(a.deltaMeters));
}

