async function renderDefinitions() {
  ["agi", "asi"].forEach((type) => {
    const list = document.querySelector(`#${type}-definition-list`);
    const starter = starterDefinitions[type].map((definition) => ({ ...definition, type }));
    list.innerHTML = starter
      .sort((a, b) => Number(b.votes || 0) - Number(a.votes || 0))
      .slice(0, 5)
      .map((definition) => {
        const content = `
            <span class="definition-copy">${escapeHtml(definition.text[language] || definition.text.en)}</span>
            <div class="definition-meta">
              <strong>${escapeHtml(
                definition.author?.[language] || definition.author?.en || copy[language].anonymousAuthor,
              )}</strong>
              <small>${
                Number(definition.votes || 0) > 0
                  ? `${Number(definition.votes)} ${copy[language].votesLabel}`
                  : copy[language].definitionCandidate
              }</small>
            </div>
        `;
        const entry = definition.source?.url
          ? `<a class="definition-entry" href="${escapeHtml(definition.source.url)}" target="_blank" rel="noreferrer" title="${escapeHtml(definition.source.name || copy[language].definitionSource)}">${content}</a>`
          : `<div class="definition-entry">${content}</div>`;
        return `<li>${entry}</li>`;
      })
      .join("");
  });
}

async function updateVotes() {
  const votes = await allVotes();
  const agiMedian = median(numericAnswers(votes, "agi"));
  const asiMedian = median(numericAnswers(votes, "asi"));
  document.querySelector("#agi-public").textContent = formatYear(agiMedian);
  document.querySelector("#asi-public").textContent = formatYear(asiMedian);
}

